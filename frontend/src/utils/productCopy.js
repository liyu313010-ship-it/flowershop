export function getProductDescription(product) {
  const name = String(product?.name ?? product?.Name ?? '').trim()
  const original = String(product?.description ?? product?.Description ?? '').trim()
  if (!name) return original || '当季鲜花精心搭配，现采现包，把新鲜与心意一起送到。'

  const exact = exactCopy.find(([key]) => name.includes(key))
  if (exact) return exact[1]

  const keyword = keywordCopy.find(([key]) => name.includes(key))
  if (keyword) return keyword[1]

  // 对没有匹配到花材的商品保留商家原文，避免误改管理员自定义文案。
  return original || `${name}精选花礼，采用当季新鲜花材，由花艺师完成搭配，适合送礼与日常装点。`
}

export default getProductDescription
